#include <stdio.h>

int main() {
    int num1, num2, sum;

    // Taking input from user
    printf("Enter the first number: ");
    scanf("%d", &num1);

    printf("Enter the second number: ");
    scanf("%d", &num2);

    // Adding two numbers
    sum = num1 + num2;

    // Printing result
    printf("The sum is: %d\n", sum);

    return 0;
}

